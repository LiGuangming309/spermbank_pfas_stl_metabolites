lapply(describe_1, function(x) {
  # 计算每个唯一值的出现次数
  counts <- table(x)
  table(x)  %>% 
    as.data.frame() -> res1
  # 计算百分比
  percentages <- round(prop.table(counts) * 100,digits =2)
  
  pt <- paste0(counts," (",percentages,")")
  # 返回计数和百分比结果
  cbind(counts = counts, percentages = percentages,pt) -> res2
    cbind(res1,res2)
  }) %>% 
  write.xlsx("result/20240407_PFOA_sperm/Multi_linear_regression_20240407/describe/table1.xlsx")
  
